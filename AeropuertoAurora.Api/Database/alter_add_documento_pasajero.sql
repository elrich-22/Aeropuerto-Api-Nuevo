DECLARE
    v_count NUMBER := 0;
BEGIN
    SELECT COUNT(*)
      INTO v_count
      FROM USER_TABLES
     WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO';

    IF v_count = 0 THEN
        EXECUTE IMMEDIATE '
            CREATE TABLE AER_DOCUMENTO_PASAJERO
            (
                DCP_ID_DOCUMENTO      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1 INCREMENT BY 1 NOCACHE),
                DCP_ID_PASAJERO       NUMBER NOT NULL,
                DCP_TIPO_DOCUMENTO    VARCHAR2(20) NOT NULL,
                DCP_NUMERO_DOCUMENTO  VARCHAR2(50) NOT NULL,
                DCP_PAIS_EMISOR       VARCHAR2(60),
                DCP_FECHA_VENCIMIENTO DATE,
                DCP_ESTADO_VALIDACION VARCHAR2(20) DEFAULT ''PENDIENTE'' NOT NULL,
                DCP_FECHA_REGISTRO    TIMESTAMP(6) DEFAULT SYSTIMESTAMP,
                DCP_FECHA_VALIDACION  TIMESTAMP(6)
            )';
    END IF;
END;
/

MERGE INTO AER_DOCUMENTO_PASAJERO d
USING (
    SELECT
        p.PAS_ID_PASAJERO AS ID_PASAJERO,
        UPPER(TRIM(p.PAS_TIPO_DOCUMENTO)) AS TIPO_DOCUMENTO,
        p.PAS_NUMERO_DOCUMENTO AS NUMERO_DOCUMENTO,
        CASE
            WHEN UPPER(TRIM(p.PAS_TIPO_DOCUMENTO)) = 'PASAPORTE' THEN p.PAS_NACIONALIDAD
            ELSE 'Guatemala'
        END AS PAIS_EMISOR,
        CASE
            WHEN UPPER(TRIM(p.PAS_TIPO_DOCUMENTO)) = 'PASAPORTE' THEN ADD_MONTHS(TRUNC(SYSDATE), 24)
            ELSE NULL
        END AS FECHA_VENCIMIENTO,
        CASE
            WHEN UPPER(TRIM(p.PAS_TIPO_DOCUMENTO)) = 'PASAPORTE' THEN 'PENDIENTE'
            ELSE 'NO_REQUIERE'
        END AS ESTADO_VALIDACION
    FROM AER_PASAJERO p
) src
ON (
    d.DCP_TIPO_DOCUMENTO = src.TIPO_DOCUMENTO
    AND d.DCP_NUMERO_DOCUMENTO = src.NUMERO_DOCUMENTO
)
WHEN NOT MATCHED THEN
    INSERT (
        DCP_ID_PASAJERO,
        DCP_TIPO_DOCUMENTO,
        DCP_NUMERO_DOCUMENTO,
        DCP_PAIS_EMISOR,
        DCP_FECHA_VENCIMIENTO,
        DCP_ESTADO_VALIDACION,
        DCP_FECHA_REGISTRO,
        DCP_FECHA_VALIDACION
    )
    VALUES (
        src.ID_PASAJERO,
        src.TIPO_DOCUMENTO,
        src.NUMERO_DOCUMENTO,
        src.PAIS_EMISOR,
        src.FECHA_VENCIMIENTO,
        src.ESTADO_VALIDACION,
        SYSTIMESTAMP,
        CASE WHEN src.TIPO_DOCUMENTO = 'PASAPORTE' AND src.FECHA_VENCIMIENTO > TRUNC(SYSDATE) THEN SYSTIMESTAMP ELSE NULL END
    );

DECLARE
    v_count NUMBER := 0;
BEGIN
    SELECT COUNT(*) INTO v_count FROM USER_CONSTRAINTS WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO' AND CONSTRAINT_NAME = 'PK_AER_DOCUMENTO_PASAJERO';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE AER_DOCUMENTO_PASAJERO ADD CONSTRAINT PK_AER_DOCUMENTO_PASAJERO PRIMARY KEY (DCP_ID_DOCUMENTO)';
    END IF;

    SELECT COUNT(*) INTO v_count FROM USER_CONSTRAINTS WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO' AND CONSTRAINT_NAME = 'FK_DOCPASAJERO_PASAJERO';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE AER_DOCUMENTO_PASAJERO ADD CONSTRAINT FK_DOCPASAJERO_PASAJERO FOREIGN KEY (DCP_ID_PASAJERO) REFERENCES AER_PASAJERO (PAS_ID_PASAJERO)';
    END IF;

    SELECT COUNT(*) INTO v_count FROM USER_CONSTRAINTS WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO' AND CONSTRAINT_NAME = 'UK_DOC_PASAJERO_NUMERO';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE AER_DOCUMENTO_PASAJERO ADD CONSTRAINT UK_DOC_PASAJERO_NUMERO UNIQUE (DCP_TIPO_DOCUMENTO, DCP_NUMERO_DOCUMENTO)';
    END IF;

    SELECT COUNT(*) INTO v_count FROM USER_CONSTRAINTS WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO' AND CONSTRAINT_NAME = 'CHK_DOCPASAJERO_TIPO';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE AER_DOCUMENTO_PASAJERO ADD CONSTRAINT CHK_DOCPASAJERO_TIPO CHECK (DCP_TIPO_DOCUMENTO IN (''DPI'',''LICENCIA'',''PASAPORTE'')) ENABLE';
    END IF;

    SELECT COUNT(*) INTO v_count FROM USER_CONSTRAINTS WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO' AND CONSTRAINT_NAME = 'CHK_DOCPASAJERO_ESTADO';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE AER_DOCUMENTO_PASAJERO ADD CONSTRAINT CHK_DOCPASAJERO_ESTADO CHECK (DCP_ESTADO_VALIDACION IN (''PENDIENTE'',''VALIDADO'',''RECHAZADO'',''NO_REQUIERE'')) ENABLE';
    END IF;

    SELECT COUNT(*) INTO v_count FROM USER_CONSTRAINTS WHERE TABLE_NAME = 'AER_DOCUMENTO_PASAJERO' AND CONSTRAINT_NAME = 'CHK_DOCPASAJERO_VIGENCIA';
    IF v_count = 0 THEN
        EXECUTE IMMEDIATE 'ALTER TABLE AER_DOCUMENTO_PASAJERO ADD CONSTRAINT CHK_DOCPASAJERO_VIGENCIA CHECK (DCP_TIPO_DOCUMENTO <> ''PASAPORTE'' OR (DCP_PAIS_EMISOR IS NOT NULL AND DCP_FECHA_VENCIMIENTO IS NOT NULL)) ENABLE';
    END IF;
END;
/

COMMIT;

